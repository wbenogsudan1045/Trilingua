def _is_garbage_block(text, bbox, page_width, page_height):
    """
    Returns True if a text block should be SKIPPED:
      - Pure numbers / page numbers  (e.g. '4', '4 4', '22 22')
      - URL-only blocks
      - Fewer than 3 meaningful words
      - Very narrow blocks (likely drawing artefacts)
      - Suspiciously tiny font remnants (bbox height < 4px)
    """
    stripped = text.strip()
    # page-number pattern: '4', '4 4', '21 21'
    if _re.fullmatch(r'\d+\s*\d*', stripped):
        return True
    # URL-only
    if stripped.lower().startswith('http') or stripped.lower().startswith('www'):
        return True
    # Fewer than 3 words
    words = [w for w in stripped.split() if _re.search(r'[a-zA-Z\u0080-\uFFFF]', w)]
    if len(words) < 3:
        return True
    # Tiny bbox height (artefact)
    x0, y0, x1, y1 = bbox
    if (y1 - y0) < 4:
        return True
    return False


def _detect_columns(blocks, page_width):
    """
    Split blocks into left / right columns based on their x-center.
    Returns (left_blocks, right_blocks).
    If the page has no clear columnar split, returns (all_blocks, []).

    .. deprecated::
        Use ``detect_columns()`` instead.  This function is kept only for
        backwards-compatibility and is no longer called by the pipeline.
    """
    mid = page_width / 2
    left  = [b for b in blocks if (b['position'][0] + b['position'][2]) / 2 < mid]
    right = [b for b in blocks if (b['position'][0] + b['position'][2]) / 2 >= mid]
    # Only treat as two-column if both sides are substantial
    if len(left) >= 2 and len(right) >= 2:
        return left, right
    return blocks, []


def detect_columns(blocks: list, page_width: float) -> list:
    """
    Detect 1, 2, or 3 columns on a PDF page using x-coordinate gap analysis
    and return all blocks in correct reading order.

    Algorithm (Requirements 10.1 – 10.5):
    1. Separate full-width blocks (width >= 90% of page_width) from the rest.
    2. Sort the remaining (columnar candidate) blocks by their left x-coordinate.
    3. Scan for horizontal gaps >= 10% of page_width between consecutive blocks
       (sorted by x0) to identify up to 2 gap positions → up to 3 columns.
    4. Assign each columnar block to a column based on the gap boundaries.
    5. Fall back to single-column if any candidate column has < 2 blocks.
    6. Sort blocks within each column by top y-coordinate.
    7. Merge columns left-to-right, interleaving full-width blocks at their
       correct y-position relative to the columnar blocks.

    Parameters
    ----------
    blocks : list[dict]
        Blocks for a single page, each with a ``"position"`` key
        ``[x0, y0, x1, y1]``.
    page_width : float
        Width of the page in PDF user-space units.

    Returns
    -------
    list[dict]
        All blocks in reading order (top-to-bottom, left-to-right within
        each column, full-width blocks interleaved at their y-position).
    """
    if not blocks:
        return []

    gap_threshold = 0.10 * page_width
    full_width_threshold = 0.90 * page_width

    # ── Step 1: Separate full-width blocks from columnar candidates ──────────
    full_width = []
    columnar = []
    for b in blocks:
        x0, y0, x1, y1 = b["position"]
        block_width = x1 - x0
        if block_width >= full_width_threshold:
            full_width.append(b)
        else:
            columnar.append(b)

    # Sort full-width blocks by top y-coordinate
    full_width.sort(key=lambda b: b["position"][1])

    # ── Step 2: If no columnar blocks, return full-width sorted by y ─────────
    if not columnar:
        return full_width

    # ── Step 3: Find gap positions using x-sorted blocks ────────────────────
    # Sort columnar blocks by their left x-coordinate to find gaps
    x_sorted = sorted(columnar, key=lambda b: b["position"][0])

    # Find up to 2 gaps (supporting up to 3 columns)
    gap_positions = []  # list of x-values where a column boundary exists
    for i in range(len(x_sorted) - 1):
        right_edge_of_current = x_sorted[i]["position"][2]   # x1 of block i
        left_edge_of_next     = x_sorted[i + 1]["position"][0]  # x0 of block i+1
        gap = left_edge_of_next - right_edge_of_current
        if gap >= gap_threshold:
            # Record the midpoint of the gap as the boundary
            boundary = (right_edge_of_current + left_edge_of_next) / 2.0
            # Avoid recording duplicate/adjacent boundaries
            if not gap_positions or (boundary - gap_positions[-1]) > gap_threshold:
                gap_positions.append(boundary)
            if len(gap_positions) == 2:
                break  # max 3 columns → max 2 boundaries

    # ── Step 4: Assign columnar blocks to columns ────────────────────────────
    if not gap_positions:
        # No gaps found → single column
        columns = [columnar]
    elif len(gap_positions) == 1:
        boundary = gap_positions[0]
        col0 = [b for b in columnar if b["position"][2] <= boundary or
                (b["position"][0] + b["position"][2]) / 2 < boundary]
        col1 = [b for b in columnar if b not in col0]
        columns = [col0, col1]
    else:
        b0, b1 = gap_positions[0], gap_positions[1]
        col0 = [b for b in columnar if (b["position"][0] + b["position"][2]) / 2 < b0]
        col2 = [b for b in columnar if (b["position"][0] + b["position"][2]) / 2 >= b1]
        col1 = [b for b in columnar if b not in col0 and b not in col2]
        columns = [col0, col1, col2]

    # ── Step 5: Fall back to single-column if any column has < 2 blocks ─────
    if any(len(col) < 2 for col in columns):
        columns = [columnar]

    # ── Step 6: Sort blocks within each column by top y-coordinate ──────────
    for col in columns:
        col.sort(key=lambda b: b["position"][1])

    # ── Step 7: Merge columns left-to-right, interleave full-width blocks ────
    # Build the flat columnar reading order (left col first, then center, then right)
    columnar_ordered = []
    for col in columns:
        columnar_ordered.extend(col)

    # Now interleave full-width blocks at their correct y-position.
    # A full-width block with top y=Y should appear before any columnar block
    # whose top y > Y (Requirement 10.4).
    result = []
    fw_idx = 0
    for block in columnar_ordered:
        block_y = block["position"][1]
        # Insert any full-width blocks that should come before this columnar block
        while fw_idx < len(full_width) and full_width[fw_idx]["position"][1] <= block_y:
            result.append(full_width[fw_idx])
            fw_idx += 1
        result.append(block)
    # Append any remaining full-width blocks at the end
    while fw_idx < len(full_width):
        result.append(full_width[fw_idx])
        fw_idx += 1

    return result


def read_docx(file_path):
    """Read paragraphs and tables from a .docx file with full style and formatting metadata.

    Extracts per paragraph (Requirements 7.1, 7.4, 7.5, 7.6):
      - style_name  : para.style.name
      - alignment   : para.alignment
      - space_before: para.paragraph_format.space_before
      - space_after : para.paragraph_format.space_after
      - font_color  : run.font.color.rgb of the first run that has a color set

    Extracts all tables (Requirements 8.1):
      - Each cell stored as a table_cell block with row/col/span metadata
      - Table cells appended after paragraph blocks
    """
    doc = Document(file_path)
    blocks = []

    # ── Paragraph blocks ──────────────────────────────────────────────────────
    for para in doc.paragraphs:
        text = para.text.strip()
        if text and len(text.split()) >= 3:
            # Collect run-level font color: use the first run that has an
            # explicit RGB color set directly on the run (not inherited).
            font_color = None
            for run in para.runs:
                try:
                    rgb = run.font.color.rgb
                    if rgb is not None:
                        font_color = rgb
                        break
                except Exception:
                    pass

            style_info = {
                "bold":        para.runs[0].bold      if para.runs else False,
                "font_size":   para.runs[0].font.size if para.runs else None,
                # Paragraph-level style metadata (Req 7.1, 7.4, 7.5, 7.6)
                "style_name":  para.style.name if para.style else None,
                "alignment":   para.alignment,
                "space_before": para.paragraph_format.space_before,
                "space_after":  para.paragraph_format.space_after,
                "font_color":  font_color,
            }
            blocks.append({"type": "paragraph", "text": text, "style": style_info})

    # ── Table blocks (Requirement 8.1) ────────────────────────────────────────
    for table_index, table in enumerate(doc.tables):
        for row_idx, row in enumerate(table.rows):
            for col_idx, cell in enumerate(row.cells):
                cell_text = cell.text.strip()

                # Collect run-level bold and font_size from the first run
                # that has an explicit value set.
                cell_bold = None
                cell_font_size = None
                for para in cell.paragraphs:
                    for run in para.runs:
                        if cell_bold is None and run.bold is not None:
                            cell_bold = run.bold
                        if cell_font_size is None and run.font.size is not None:
                            cell_font_size = run.font.size
                        if cell_bold is not None and cell_font_size is not None:
                            break
                    if cell_bold is not None and cell_font_size is not None:
                        break

                # Determine row_span and col_span from the underlying XML.
                # python-docx exposes merged cells by repeating the same cell
                # object; we derive spans from the tc XML element attributes.
                try:
                    tc = cell._tc
                    # gridSpan gives the number of columns the cell spans
                    grid_span_el = tc.find(
                        './/{http://schemas.openxmlformats.org/wordprocessingml/2006/main}gridSpan'
                    )
                    col_span = int(grid_span_el.get(
                        '{http://schemas.openxmlformats.org/wordprocessingml/2006/main}val', 1
                    )) if grid_span_el is not None else 1

                    # vMerge gives vertical merge info; count consecutive vMerge
                    # cells below this one to determine row_span.
                    vmerge_el = tc.find(
                        './/{http://schemas.openxmlformats.org/wordprocessingml/2006/main}vMerge'
                    )
                    if vmerge_el is not None:
                        val = vmerge_el.get(
                            '{http://schemas.openxmlformats.org/wordprocessingml/2006/main}val', ''
                        )
                        if val == 'restart':
                            # Count how many rows below continue this merge
                            row_span = 1
                            for next_row in table.rows[row_idx + 1:]:
                                if col_idx < len(next_row.cells):
                                    next_tc = next_row.cells[col_idx]._tc
                                    next_vm = next_tc.find(
                                        './/{http://schemas.openxmlformats.org/wordprocessingml/2006/main}vMerge'
                                    )
                                    if next_vm is not None and next_vm.get(
                                        '{http://schemas.openxmlformats.org/wordprocessingml/2006/main}val', ''
                                    ) != 'restart':
                                        row_span += 1
                                    else:
                                        break
                        else:
                            # Continuation cell — skip to avoid duplicates
                            continue
                    else:
                        row_span = 1
                except Exception:
                    col_span = 1
                    row_span = 1

                blocks.append({
                    "type":        "table_cell",
                    "text":        cell_text,
                    "table_index": table_index,
                    "row":         row_idx,
                    "col":         col_idx,
                    "row_span":    row_span,
                    "col_span":    col_span,
                    "style": {
                        "bold":      cell_bold,
                        "font_size": cell_font_size,
                    },
                })

    return blocks


def read_pdf(file_path, column_mode="auto"):
    """
    Read text blocks from a PDF with:
      - Accurate full-block bounding boxes (get_text 'dict')
      - Garbage-block filtering (artefacts, page numbers, URLs)
      - Two-column detection  (column_mode: 'auto'|'single'|'left'|'right')

    column_mode:
      'auto'   – detect columns automatically; translate ALL text found
      'single' – treat entire page as one column
      'left'   – only translate left-column text (useful for bilingual PDFs
                 where the left side is the source language)
      'right'  – only translate right-column text
    """
    blocks = []
    doc = fitz.open(file_path)

    for page_num in range(len(doc)):
        page = doc[page_num]
        pw   = page.rect.width
        ph   = page.rect.height

        page_dict = page.get_text(
            "dict",
            flags=fitz.TEXT_PRESERVE_WHITESPACE | fitz.TEXT_PRESERVE_LIGATURES
        )

        raw_blocks = []
        for rb in page_dict.get("blocks", []):
            if rb.get("type") != 0:
                continue
            lines = rb.get("lines", [])
            if not lines:
                continue

            parts = []
            dom_size  = 11.0
            dom_color = 0
            dom_bold  = False
            dom_font  = "helv"  # default; overwritten by the dominant span
            for line in lines:
                lt = ""
                for span in line.get("spans", []):
                    st = span.get("text", "")
                    if st.strip():
                        lt += st
                        dom_size  = span.get("size",  dom_size)
                        dom_color = span.get("color", dom_color)
                        dom_bold  = bool(span.get("flags", 0) & 2**4)
                        # Capture the font name from the dominant (last
                        # non-empty) span — Requirement 4.1 / 4.5.
                        span_font = span.get("font", "")
                        if span_font:
                            dom_font = span_font
                if lt.strip():
                    parts.append(lt.strip())

            block_text = " ".join(parts).strip()
            if not block_text:
                continue

            bbox = list(rb["bbox"])

            if _is_garbage_block(block_text, bbox, pw, ph):
                continue

            dom_size = max(6.0, min(float(dom_size or 11.0), 72.0))

            raw_blocks.append({
                "type":     "paragraph",
                "text":     block_text,
                "position": bbox,
                "page":     page_num,
                "style":    {"font_size": dom_size, "font": dom_font,
                             "color": dom_color, "bold": dom_bold},
            })

        # Column filtering
        if column_mode == "single" or len(raw_blocks) == 0:
            selected = raw_blocks
        elif column_mode == "auto":
            # Use the new gap-analysis column detector (Requirements 10.1–10.5).
            # detect_columns() returns all blocks in correct reading order
            # (full-width blocks interleaved, columns merged left-to-right).
            selected = detect_columns(raw_blocks, pw)
        else:
            # 'left' / 'right' modes still use the legacy x-center split so
            # that bilingual-PDF workflows (translate only one side) continue
            # to work unchanged.
            left_blocks, right_blocks = _detect_columns(raw_blocks, pw)
            if column_mode == "left":
                selected = left_blocks
            else:   # 'right'
                selected = right_blocks

        blocks.extend(selected)

    doc.close()
    return blocks


def read_txt(file_path):
    with open(file_path, "r", encoding="utf-8", errors="replace") as f:
        raw = f.read()
    paragraphs = [p.strip() for p in _re.split(r'\n{2,}', raw) if p.strip()]
    if not paragraphs:
        paragraphs = [l.strip() for l in raw.splitlines() if l.strip()]
    return [{"type": "paragraph", "text": p, "style": {}} for p in paragraphs
            if len(p.split()) >= 3]


def read_rtf(file_path):
    from striprtf.striprtf import rtf_to_text
    with open(file_path, "r", encoding="utf-8", errors="replace") as f:
        raw = f.read()
    plain = rtf_to_text(raw)
    paragraphs = [p.strip() for p in plain.splitlines() if p.strip()]
    return [{"type": "paragraph", "text": p, "style": {}} for p in paragraphs
            if len(p.split()) >= 3]


def read_odt(file_path):
    from odf.opendocument import load as odf_load
    from odf.text import P
    from odf import teletype
    doc = odf_load(file_path)
    blocks = []
    for para in doc.getElementsByType(P):
        text = teletype.extractText(para).strip()
        if text and len(text.split()) >= 3:
            blocks.append({"type": "paragraph", "text": text, "style": {}})
    return blocks


def read_csv(file_path):
    rows = []
    with open(file_path, "r", encoding="utf-8", errors="replace", newline="") as f:
        reader = csv.reader(f)
        for row in reader:
            rows.append(row)
    return {"type": "csv", "data": rows}

