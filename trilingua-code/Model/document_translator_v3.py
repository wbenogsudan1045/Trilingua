def _remove_repetition_loops(text, max_repeat=3):
    """
    Detect and strip repeated phrase loops that NLLB sometimes generates.
    Example: 'What are possible answers? What are possible answers? ...' × 40

    Strategy:
      1. Split into sentences.
      2. Keep a sentence only if it hasn't appeared in the last `max_repeat` sentences.
      3. Hard-stop when we see the same sentence for the 2nd time.
    """
    sentences = _re2.split(r'(?<=[.!?])\s+', text.strip())
    seen   = {}
    result = []
    for s in sentences:
        key = s.strip().lower()
        if not key:
            continue
        count = seen.get(key, 0)
        if count >= max_repeat:
            break   # hard-stop: this is a loop
        seen[key] = count + 1
        result.append(s)
    return " ".join(result)


class Glossary_Store:
    """
    Holds source→target term pairs and applies post-translation substitution.

    Raises ValueError on duplicate source terms (case-insensitive).
    Raises ValueError when len(pairs) > 1000.
    """

    def __init__(self, pairs: list | None = None):
        """
        pairs: list of (source_term, target_term).
        Raises ValueError if duplicates exist (case-insensitive source key).
        Raises ValueError if len(pairs) > 1000.
        """
        if pairs is None:
            pairs = []

        if len(pairs) > 1000:
            raise ValueError(
                f"Glossary_Store: too many term pairs ({len(pairs)}); maximum is 1000."
            )

        self._terms: list[tuple[str, str]] = []
        seen: dict[str, str] = {}  # lowercase key → original source term

        for source, target in pairs:
            key = source.lower()
            if key in seen:
                raise ValueError(
                    f"Glossary_Store: duplicate source term '{source}' "
                    f"(already registered as '{seen[key]}')."
                )
            seen[key] = source
            self._terms.append((source, target))

        # Pre-compile a single regex that matches any source term as a whole word.
        # Terms are sorted longest-first so longer matches take priority.
        if self._terms:
            sorted_terms = sorted(self._terms, key=lambda p: len(p[0]), reverse=True)
            pattern = "|".join(
                r"\b" + re.escape(src) + r"\b" for src, _ in sorted_terms
            )
            self._regex = re.compile(pattern, re.IGNORECASE)
            # Map lowercase source → target for fast lookup
            self._lookup: dict[str, str] = {
                src.lower(): tgt for src, tgt in sorted_terms
            }
        else:
            self._regex = None
            self._lookup = {}

    def apply(self, text: str) -> str:
        """
        Replace whole-word occurrences of each source term in `text`
        with the corresponding target term, preserving capitalisation.
        Returns the substituted text.
        """
        if self._regex is None:
            return text

        def _replace(match: re.Match) -> str:
            original_token = match.group(0)
            target_term = self._lookup[original_token.lower()]
            return self._match_case(original_token, target_term)

        return self._regex.sub(_replace, text)

    @staticmethod
    def _match_case(original_token: str, target_term: str) -> str:
        """Apply capitalisation pattern of original_token to target_term."""
        if original_token.isupper():
            return target_term.upper()
        if original_token.istitle():
            return target_term.title()
        if original_token.islower():
            return target_term.lower()
        # Any other pattern → return target as stored
        return target_term


# ── Font_Mapper ───────────────────────────────────────────────────────────────

class Font_Mapper:
    """
    Maps PDF font names to usable font identifiers for ``insert_textbox()``.

    Resolution order (Requirements 4.1 – 4.5):
    1. Case-insensitive exact match against ``embedded_fonts`` → return ``font_name``
       as-is so PyMuPDF uses the embedded font directly.
    2. Keyword classification:
       - sans-serif keywords → ``"helv"``  (Helvetica)
       - serif keywords      → ``"tiro"``  (Times-Roman)
       - mono keywords       → ``"cour"``  (Courier)
    3. Unknown font or any exception → return ``"helv"`` and log a warning.
    """

    SANS_SERIF_KEYWORDS: set = {
        "arial", "helvetica", "calibri", "verdana", "tahoma", "gothic", "sans"
    }
    SERIF_KEYWORDS: set = {
        "times", "georgia", "garamond", "palatino", "roman", "serif", "minion"
    }
    MONO_KEYWORDS: set = {
        "courier", "mono", "consolas", "menlo", "inconsolata"
    }

    def resolve(self, font_name: str, embedded_fonts: set,
                page: int = 0, bbox=None) -> str:
        """
        Return the fontname string to pass to ``insert_textbox()``.

        Parameters
        ----------
        font_name : str
            The dominant font name extracted from the block's span metadata.
        embedded_fonts : set[str]
            The set of font names embedded in the original PDF page, obtained
            via ``fitz.Document.get_page_fonts()``.
        page : int
            Page number (0-based) — used only in warning messages.
        bbox : sequence or None
            Block bounding box — used only in warning messages.

        Returns
        -------
        str
            A font identifier suitable for ``insert_textbox()``.
        """
        try:
            # ── Step 1: case-insensitive exact match against embedded fonts ──
            font_name_lower = font_name.lower()
            for ef in embedded_fonts:
                if ef.lower() == font_name_lower:
                    return font_name  # use the original casing as stored

            # ── Step 2: keyword classification ───────────────────────────────
            fn_lower = font_name_lower
            for keyword in self.SANS_SERIF_KEYWORDS:
                if keyword in fn_lower:
                    return "helv"
            for keyword in self.SERIF_KEYWORDS:
                if keyword in fn_lower:
                    return "tiro"
            for keyword in self.MONO_KEYWORDS:
                if keyword in fn_lower:
                    return "cour"

            # ── Step 3: unknown font → fall back to Helvetica ─────────────
            print(
                f"  ⚠️  Font_Mapper: unrecognised font '{font_name}' "
                f"on page {page}, bbox {bbox} — falling back to 'helv'."
            )
            return "helv"

        except Exception as exc:  # noqa: BLE001
            print(
                f"  ⚠️  Font_Mapper: exception resolving font '{font_name}' "
                f"on page {page}, bbox {bbox}: {exc} — falling back to 'helv'."
            )
            return "helv"


# ── Background_Sampler ───────────────────────────────────────────────────────

class Background_Sampler:
    """
    Samples the pixel color at the four corners of a block's bounding box
    to determine the correct erase fill color for the PDF writer.

    Requirements 5.1 – 5.6:
    - Renders the page to a pixmap via ``page.get_pixmap()``.
    - Clamps corner coordinates to page bounds before sampling.
    - Returns ``(r, g, b)`` floats in ``[0, 1]`` when all four corners share
      the same RGB color (exact equality of all three channels).
    - Returns ``None`` when corners differ (non-uniform background).
    - Returns ``None`` (no exception) for zero-width or zero-height bbox.
    - Returns ``None`` and logs a warning on any exception.
    """

    def sample(
        self,
        page: "fitz.Page",
        bbox: "tuple[float, float, float, float]",
    ) -> "tuple[float, float, float] | None":
        """
        Sample the four corners of *bbox* on *page* and return a uniform
        background color, or ``None`` if the background is non-uniform.

        Parameters
        ----------
        page : fitz.Page
            The PyMuPDF page object to sample.
        bbox : tuple[float, float, float, float]
            Bounding box ``(x0, y0, x1, y1)`` in PDF user-space coordinates.

        Returns
        -------
        tuple[float, float, float] | None
            ``(r, g, b)`` floats in ``[0, 1]`` if all four corners match;
            ``None`` otherwise.
        """
        try:
            x0, y0, x1, y1 = bbox

            # Return None (no exception) for degenerate bboxes (Req 5.5).
            if x1 <= x0 or y1 <= y0:
                return None

            # Render the page to a pixmap (Req 5.1).
            pix = page.get_pixmap()
            pw = pix.width
            ph = pix.height

            # Page bounds for clamping (Req 5.1).
            page_rect = page.rect
            page_x0 = page_rect.x0
            page_y0 = page_rect.y0
            page_x1 = page_rect.x1
            page_y1 = page_rect.y1

            # The pixmap covers the page rect; we need to convert PDF
            # coordinates to pixel coordinates.
            # pixel_x = (pdf_x - page_x0) / (page_x1 - page_x0) * (pw - 1)
            page_w = page_x1 - page_x0
            page_h = page_y1 - page_y0

            def _pdf_to_pixel(px, py):
                """Convert PDF coordinates to integer pixel indices, clamped."""
                # Clamp to page bounds first (Req 5.1)
                cx = max(page_x0, min(px, page_x1))
                cy = max(page_y0, min(py, page_y1))
                # Map to pixel space
                ix = int((cx - page_x0) / page_w * (pw - 1)) if page_w > 0 else 0
                iy = int((cy - page_y0) / page_h * (ph - 1)) if page_h > 0 else 0
                # Final clamp to pixmap dimensions
                ix = max(0, min(ix, pw - 1))
                iy = max(0, min(iy, ph - 1))
                return ix, iy

            # Sample the four corners of the bbox.
            corners = [
                _pdf_to_pixel(x0, y0),  # top-left
                _pdf_to_pixel(x1, y0),  # top-right
                _pdf_to_pixel(x0, y1),  # bottom-left
                _pdf_to_pixel(x1, y1),  # bottom-right
            ]

            # Read pixel samples; pix.pixel(x, y) returns a tuple of channel
            # values (0–255 per channel for RGB/RGBA pixmaps).
            samples = [pix.pixel(cx, cy) for cx, cy in corners]

            # Normalise to [0, 1] using the first three channels (R, G, B).
            def _to_float(sample):
                return (sample[0] / 255.0, sample[1] / 255.0, sample[2] / 255.0)

            ref = _to_float(samples[0])
            for s in samples[1:]:
                if _to_float(s) != ref:
                    # Corners differ → non-uniform background (Req 5.2 / 5.3)
                    return None

            # All four corners match → return the uniform color (Req 5.2).
            return ref

        except Exception as exc:  # noqa: BLE001
            print(
                f"  ⚠️  Background_Sampler: exception sampling bbox {bbox}: "
                f"{exc} — falling back to white fill."
            )
            return None


def _split_long_text(text, max_words=80):
    """
    Break a long paragraph into sentence-boundary chunks of ≤ max_words.
    This prevents NLLB from trying to fit a 400-word paragraph into one decode.

    .. deprecated::
        Use ``Chunk_Splitter().split()`` instead.  This function is kept only
        for backwards-compatibility and is no longer called by the pipeline.
    """
    sentences = _re2.split(r'(?<=[.!?])\s+', text.strip())
    chunks, cur, cur_len = [], [], 0
    for s in sentences:
        wc = len(s.split())
        if cur_len + wc > max_words and cur:
            chunks.append(" ".join(cur))
            cur, cur_len = [], 0
        cur.append(s)
        cur_len += wc
    if cur:
        chunks.append(" ".join(cur))
    return chunks


class Chunk_Splitter:
    """
    Splits a block of text into sentence-boundary-aligned chunks.

    Rules (Requirements 1.1 – 1.6):
    - Split only at sentence-ending punctuation (. ! ?) followed by
      whitespace or end-of-string.
    - Preferred maximum chunk size is ``max_tokens`` whitespace-delimited
      tokens.
    - When no sentence boundary exists within ``max_tokens`` tokens, extend
      to the next available boundary up to ``hard_cap`` tokens.
    - When no sentence-ending punctuation exists anywhere in the text, return
      ``[text]`` (the whole block as a single chunk).
    - Enforce a minimum chunk size of ``min_tokens`` tokens by merging a
      too-small chunk forward into the next chunk.
    - The complete character-for-character content of the original block is
      preserved across all chunks (no characters dropped or duplicated).
    """

    # Matches the position immediately after . ! ? when followed by
    # whitespace or end-of-string.
    SENTENCE_END = re.compile(r'(?<=[.!?])(?=\s|$)')

    def split(self, text: str, max_tokens: int = 80, hard_cap: int = 150,
              min_tokens: int = 5) -> list:
        """
        Split *text* into chunks respecting sentence boundaries.

        Parameters
        ----------
        text : str
            The source block text to split.
        max_tokens : int
            Preferred maximum number of whitespace-delimited tokens per chunk.
        hard_cap : int
            Absolute maximum tokens per chunk when no boundary is found within
            ``max_tokens``.
        min_tokens : int
            Minimum tokens per chunk; chunks smaller than this are merged
            forward into the following chunk.

        Returns
        -------
        list[str]
            A list of chunk strings whose concatenation (joined with a single
            space after normalising whitespace) equals the original text.
        """
        # Normalise internal whitespace so token counts are consistent, but
        # preserve the original text for the no-boundary fast-path.
        words = text.split()
        total = len(words)

        # Fast-path 1: text is short enough to be a single chunk.
        if total <= max_tokens:
            return [text]

        # Fast-path 2: no sentence-ending punctuation anywhere → single chunk.
        if not self.SENTENCE_END.search(text):
            return [text]

        # ── Build candidate split points ──────────────────────────────────
        # We work on the *word* level.  First, reconstruct a normalised
        # version of the text (words joined by single spaces) so that
        # character-level regex positions map cleanly to word indices.
        normalised = " ".join(words)

        # Find all character positions in `normalised` that are valid
        # sentence-end split points.
        boundary_char_positions = [m.start() for m in self.SENTENCE_END.finditer(normalised)]

        # Convert character positions to word indices.
        # A boundary at char position p means the split happens *after* the
        # word that ends at or before p.  We find the word index by counting
        # spaces up to p.
        boundary_word_indices = []
        for pos in boundary_char_positions:
            # Count how many words are in normalised[:pos]
            word_idx = len(normalised[:pos].split())
            if word_idx > 0 and word_idx not in boundary_word_indices:
                boundary_word_indices.append(word_idx)

        if not boundary_word_indices:
            # No usable boundaries found after conversion → single chunk.
            return [text]

        # ── Greedy chunking ───────────────────────────────────────────────
        chunks = []
        start = 0  # word index of the current chunk's start

        while start < total:
            remaining = total - start

            # If what's left fits within max_tokens, take it all.
            if remaining <= max_tokens:
                chunks.append(" ".join(words[start:]))
                break

            # Look for the last boundary within [start+1 .. start+max_tokens].
            preferred_end = start + max_tokens  # exclusive upper bound (word index)
            candidate = None
            for bi in boundary_word_indices:
                if start < bi <= preferred_end:
                    candidate = bi  # keep the *last* one within the window

            if candidate is not None:
                # Found a boundary within the preferred window.
                chunks.append(" ".join(words[start:candidate]))
                start = candidate
                continue

            # No boundary within max_tokens → extend to hard_cap.
            hard_end = start + hard_cap
            for bi in boundary_word_indices:
                if preferred_end < bi <= hard_end:
                    candidate = bi
                    break  # take the *first* one beyond max_tokens

            if candidate is not None:
                chunks.append(" ".join(words[start:candidate]))
                start = candidate
                continue

            # No boundary within hard_cap either → force-split at hard_cap.
            end = min(start + hard_cap, total)
            chunks.append(" ".join(words[start:end]))
            start = end

        # ── Enforce minimum chunk size ────────────────────────────────────
        # Merge any chunk that is too small into the *next* chunk.
        merged: list = []
        for chunk in chunks:
            if merged and len(merged[-1].split()) < min_tokens:
                # Previous chunk is too small → merge current into it.
                merged[-1] = merged[-1] + " " + chunk
            else:
                merged.append(chunk)

        # Edge case: the *last* chunk may still be too small after the loop.
        # Merge it backward into the second-to-last chunk.
        if len(merged) > 1 and len(merged[-1].split()) < min_tokens:
            last = merged.pop()
            merged[-1] = merged[-1] + " " + last

        return merged


# ── Context_Buffer ────────────────────────────────────────────────────────────

class Context_Buffer:
    """
    Sliding window of the last N translated blocks used as a context hint
    for the next translation call.

    window_size: number of preceding translated blocks to retain (default 2).
    """

    def __init__(self, window_size: int = 2):
        self._buffer: deque = deque(maxlen=window_size)

    def push(self, translated_text: str) -> None:
        """Add a newly translated block to the buffer."""
        self._buffer.append(translated_text)

    def get_hint(self) -> str:
        """Return buffer contents joined by ' ||| ', or '' if empty."""
        if not self._buffer:
            return ""
        return " ||| ".join(self._buffer)

    def clear(self) -> None:
        """Reset the buffer (called at the start of each document job)."""
        self._buffer.clear()


# ── Core translation ──────────────────────────────────────────────────────────

def _translate_single(text, src_code, tgt_code, context_hint=""):
    """
    Translate a single piece of text with anti-hallucination generation params.

    Key improvements over previous version:
      - repetition_penalty=1.3  → penalises repeated n-grams during beam search
      - no_repeat_ngram_size=4  → hard-blocks any 4-gram from repeating
      - Dynamic max_new_tokens  → capped at 2× the input length, not a fixed 400
      - Splits long paragraphs before encoding so NLLB never tries to decode
        a 400-token paragraph in one shot (which is what caused the 'winds' loop)
      - context_hint: prepended to source text with ' ||| ' delimiter; truncated
        from the front when combined token count exceeds 400 (Requirement 2.2–2.3)
    """
    tokenizer.src_lang = src_code
    forced_id = tokenizer.convert_tokens_to_ids(tgt_code)

    # Split the source text into sentence-boundary chunks first (Req 1.1–1.6).
    # The context hint is applied per-chunk below so that the hint is only
    # prepended to the first chunk (the hint is about the preceding blocks,
    # not about each individual chunk of the current block).
    text_chunks = Chunk_Splitter().split(text)

    # ── Context hint: prepend to first chunk and enforce 400-token budget ─────
    # Build the first chunk's input with the context hint prepended.
    # If the combined token count exceeds 400, truncate the hint from the front
    # (oldest context first) one word at a time until it fits.
    chunks = []
    for idx, chunk in enumerate(text_chunks):
        if context_hint and idx == 0:
            combined = context_hint + " ||| " + chunk
            # Check token count; truncate hint from front if over budget
            token_ids = tokenizer(combined, return_tensors="pt",
                                  add_special_tokens=True)["input_ids"][0]
            if len(token_ids) > 400:
                hint_words = context_hint.split()
                while hint_words:
                    hint_words.pop(0)  # drop oldest word from front
                    if hint_words:
                        combined = " ".join(hint_words) + " ||| " + chunk
                    else:
                        combined = chunk
                    token_ids = tokenizer(combined, return_tensors="pt",
                                          add_special_tokens=True)["input_ids"][0]
                    if len(token_ids) <= 400:
                        break
            chunks.append(combined)
        else:
            chunks.append(chunk)

    translated_chunks = []
    for chunk in chunks:
        inputs = tokenizer(
            chunk,
            return_tensors="pt",
            padding=True,
            truncation=True,
            max_length=400,
        ).to(device)

        input_len = inputs["input_ids"].shape[1]
        # Cap output at 2× the source tokens — prevents runaway generation
        max_new = min(max(input_len * 2, 30), 400)

        with torch.no_grad():
            output_ids = model.generate(
                **inputs,
                forced_bos_token_id=forced_id,
                max_new_tokens=max_new,
                num_beams=4,
                early_stopping=True,
                repetition_penalty=1.3,      # ← penalises repeat phrases
                no_repeat_ngram_size=4,      # ← hard-blocks 4-gram repeats
                length_penalty=1.0,
            )

        raw = tokenizer.batch_decode(output_ids, skip_special_tokens=True)[0]
        clean = _remove_repetition_loops(raw)
        translated_chunks.append(clean)

    return " ".join(translated_chunks)


def batch_translate_blocks(blocks, source_lang, target_lang, batch_size=4,
                           context_window=1, glossary_store=None,
                           context_buffer=None):
    """
    Translate all blocks with context-aware batching.

    context_window: number of preceding blocks to show as context hint
    (currently passed as metadata; can be used for prompt-based models).
    glossary_store: optional Glossary_Store instance; when provided, apply()
    is called on each translated block text after _translate_single() returns.
    context_buffer: optional Context_Buffer instance; when provided, the hint
    from the buffer is prepended to each source block before encoding, and
    each translated block is pushed into the buffer after translation.
    """
    if not blocks:
        return []

    src_code = LANGUAGES[source_lang]
    tgt_code = LANGUAGES[target_lang]
    total = len(blocks)
    translated_blocks = []

    for i, block in enumerate(blocks):
        print(f"  Translating block {i+1}/{total}...", end="\r", flush=True)

        # Retrieve context hint from the buffer (empty string if no buffer or empty)
        hint = context_buffer.get_hint() if context_buffer is not None else ""

        translated_text = _translate_single(
            block["text"], src_code, tgt_code, context_hint=hint
        )

        # Push the translated text into the context buffer for subsequent blocks
        if context_buffer is not None:
            context_buffer.push(translated_text)

        # Apply glossary substitution after translation
        if glossary_store is not None:
            translated_text = glossary_store.apply(translated_text)

        new_block = {
            "type":  block["type"],
            "text":  translated_text,
            "style": block.get("style", {}),
        }
        if "position" in block:
            new_block["position"] = block["position"]
            new_block["page"]     = block["page"]
        translated_blocks.append(new_block)

    print(f"\n  ✓ Translation complete! ({total} blocks)")
    return translated_blocks

