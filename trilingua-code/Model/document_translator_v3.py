# ── Style_Mapper ──────────────────────────────────────────────────────────────

class Style_Mapper:
    """
    Maps DOCX paragraph style names from the source document to the output
    document's available styles.

    Requirements 7.1, 7.2, 7.3:
    - Return ``style_name`` unchanged when it exists in ``available_styles``.
    - Return ``"Normal"`` when ``style_name`` is ``None``, an empty string,
      or a name that does not exist in ``available_styles``.
    """

    def resolve(self, style_name: str | None, available_styles: set) -> str:
        """
        Return the style name to apply to the output paragraph.

        Parameters
        ----------
        style_name : str | None
            The paragraph style name extracted from the source document.
        available_styles : set[str]
            The set of style names present in the output document.

        Returns
        -------
        str
            ``style_name`` if it is in ``available_styles``; ``"Normal"``
            otherwise (including when ``style_name`` is ``None`` or ``""``).
        """
        if not style_name:
            return "Normal"
        if style_name in available_styles:
            return style_name
        return "Normal"


def write_docx(blocks, output_file, original_file=None):
    """Write blocks to a DOCX file with preserved styles, formatting, and tables.

    Reconstructs:
    - Paragraphs with paragraph style (via Style_Mapper), alignment,
      space_before, space_after, and run-level font color (Requirements 7.2–7.6).
    - Tables grouped by table_index, with merged cell spans and run-level
      bold / font_size (Requirements 8.2–8.6).

    Parameters
    ----------
    blocks : list[dict]
        Translated blocks as produced by ``batch_translate_blocks()``.
        Each block has ``"type"`` of ``"paragraph"`` or ``"table_cell"``.
    output_file : str
        Path to write the output ``.docx`` file.
    original_file : str | None
        Unused; kept for API compatibility.
    """
    doc = Document()
    style_mapper = Style_Mapper()

    # Build the set of available style names in the output document once.
    available_styles: set = {s.name for s in doc.styles}

    # ── Separate paragraph blocks from table_cell blocks ─────────────────────
    paragraph_blocks = [b for b in blocks if b.get("type") == "paragraph"]
    table_blocks     = [b for b in blocks if b.get("type") == "table_cell"]

    # ── Write paragraph blocks ────────────────────────────────────────────────
    for block in paragraph_blocks:
        style_info = block.get("style") or {}

        # Resolve paragraph style (Requirements 7.2, 7.3)
        style_name = style_info.get("style_name")
        resolved_style = style_mapper.resolve(style_name, available_styles)

        # Add paragraph with the resolved style.
        para = doc.add_paragraph(block["text"], style=resolved_style)

        # Copy alignment when set (Requirement 7.4)
        alignment = style_info.get("alignment")
        if alignment is not None:
            para.alignment = alignment

        # Copy paragraph spacing when set (Requirement 7.5)
        space_before = style_info.get("space_before")
        if space_before is not None:
            para.paragraph_format.space_before = space_before

        space_after = style_info.get("space_after")
        if space_after is not None:
            para.paragraph_format.space_after = space_after

        # Apply run-level formatting (bold, font_size, font_color)
        if para.runs:
            run = para.runs[0]

            # Bold (only set when explicitly True in source)
            if style_info.get("bold"):
                run.bold = True

            # Font size (only set when explicitly provided)
            font_size = style_info.get("font_size")
            if font_size is not None:
                run.font.size = font_size

            # Font color (Requirement 7.6)
            font_color = style_info.get("font_color")
            if font_color is not None:
                run.font.color.rgb = font_color

    # ── Write table blocks (Requirements 8.2–8.6) ────────────────────────────
    if table_blocks:
        # Group cells by table_index, preserving insertion order.
        tables_dict: dict = {}
        for cell_block in table_blocks:
            tidx = cell_block["table_index"]
            if tidx not in tables_dict:
                tables_dict[tidx] = []
            tables_dict[tidx].append(cell_block)

        # Process tables in ascending table_index order (Requirement 8.6).
        for tidx in sorted(tables_dict.keys()):
            cells = tables_dict[tidx]

            # Determine table dimensions from the cell metadata.
            max_row = max(c["row"] for c in cells)
            max_col = max(c["col"] for c in cells)
            num_rows = max_row + 1
            num_cols = max_col + 1

            # Add the table to the document (appended at current body end).
            table = doc.add_table(rows=num_rows, cols=num_cols)

            # Build a lookup: (row, col) → cell_block for fast access.
            cell_map: dict = {(c["row"], c["col"]): c for c in cells}

            for row_idx in range(num_rows):
                for col_idx in range(num_cols):
                    cell_block = cell_map.get((row_idx, col_idx))
                    if cell_block is None:
                        continue

                    tbl_cell = table.cell(row_idx, col_idx)

                    # Write cell text (Requirement 8.2: empty cells stay empty)
                    cell_text = cell_block.get("text", "").strip()
                    if cell_text:
                        # Clear the default empty paragraph and set text.
                        tbl_cell.paragraphs[0].clear()
                        run = tbl_cell.paragraphs[0].add_run(cell_text)

                        # Apply run-level bold and font_size (Requirement 8.4)
                        cell_style = cell_block.get("style") or {}
                        cell_bold = cell_style.get("bold")
                        if cell_bold is not None:
                            run.bold = cell_bold

                        cell_font_size = cell_style.get("font_size")
                        if cell_font_size is not None:
                            run.font.size = cell_font_size

                    # Apply merged cell spans (Requirement 8.3)
                    col_span = cell_block.get("col_span", 1)
                    row_span = cell_block.get("row_span", 1)

                    if col_span > 1:
                        # Merge horizontally: merge this cell with the cells
                        # to its right.
                        end_col = col_idx + col_span - 1
                        if end_col < num_cols:
                            merge_target = table.cell(row_idx, end_col)
                            tbl_cell.merge(merge_target)

                    if row_span > 1:
                        # Merge vertically: merge this cell with the cells below.
                        end_row = row_idx + row_span - 1
                        if end_row < num_rows:
                            # After a horizontal merge, tbl_cell may have
                            # changed; re-fetch from the table.
                            tbl_cell_v = table.cell(row_idx, col_idx)
                            merge_target_v = table.cell(end_row, col_idx)
                            tbl_cell_v.merge(merge_target_v)

    doc.save(output_file)


def _resolve_overflow(
    page,
    block_text: str,
    x0: float, y0: float, x1: float, y1: float,
    font_size: float,
    resolved_font: str,
    other_bboxes: list,
    page_height: float,
    original_doc,
    page_num: int,
    bg_color,
    initial_remaining: float,
) -> dict:
    """
    Resolve text overflow for a single block using the containment algorithm
    (Requirements 6.1–6.6).

    This function is extracted from ``write_pdf_preserved()`` so that the
    overflow logic can be unit-tested independently of the full PDF pipeline.

    Algorithm:
      1. ``insert_textbox()`` with original rect; if ``remaining >= 0``, done.
      2. Check if expanding downward by ``abs(remaining) + font_size`` would
         bring the bottom within 2pt of any other block on the page.
      3. If no collision: expand rect and re-insert.
      4. If collision: reduce ``font_size`` by 1pt and retry from step 1;
         repeat until ``font_size == 6``.
      5. If still overflowing at 6pt: insert remaining text in a continuation
         box of the same width placed 4pt below the last block on the page.
      6. If continuation box extends beyond page bottom: clip to page boundary
         and log a warning.

    Parameters
    ----------
    page : fitz.Page
        The output page to draw on.
    block_text : str
        The translated text to insert.
    x0, y0, x1, y1 : float
        Original block bounding box coordinates.
    font_size : float
        Starting font size (points).
    resolved_font : str
        Font name to use for ``insert_textbox()``.
    other_bboxes : list[list[float]]
        Bounding boxes of all other blocks on the page (read-only).
    page_height : float
        Height of the page in points.
    original_doc : fitz.Document
        The original PDF document (used for clip-and-redraw background).
    page_num : int
        0-based page number (used for clip-and-redraw and warning messages).
    bg_color : tuple[float, float, float] | None
        Uniform background color, or ``None`` for clip-and-redraw.
    initial_remaining : float
        The ``remaining`` value returned by the initial ``insert_textbox()``
        call (must be negative to enter this function).

    Returns
    -------
    dict with keys:
        ``resolved``      : bool — True if text fits without a continuation box.
        ``final_font``    : float — The font size used for the final insertion.
        ``expanded``      : bool — True if the rect was expanded downward.
        ``continuation``  : bool — True if a continuation box was created.
        ``clipped``       : bool — True if the continuation box was clipped.
        ``cont_rect``     : fitz.Rect | None — The continuation box rect, if any.
    """
    MIN_FONT = 6.0

    def _would_collide(new_bottom: float) -> bool:
        """Return True if expanding to new_bottom would be within 2pt of any
        block below the current block (Requirement 6.2)."""
        for ob in other_bboxes:
            ob_top = ob[1]  # y0 of the other block
            if ob_top > y0:  # only blocks below current
                if ob_top - new_bottom < 2:
                    return True
        return False

    def _erase_and_redraw(rect: "fitz.Rect") -> None:
        """Erase the given rect using the background color or clip-and-redraw."""
        if bg_color is not None:
            page.draw_rect(rect, color=None, fill=bg_color)
        else:
            page.show_pdf_page(
                rect, original_doc, page_num,
                clip=rect, keep_proportion=True,
            )

    current_font = font_size
    fit_remaining = initial_remaining  # negative at entry
    resolved = False
    expanded = False
    continuation = False
    clipped = False
    cont_rect = None

    # Step 1: if initial_remaining >= 0, text already fits — nothing to do.
    if initial_remaining >= 0:
        return {
            "resolved":     True,
            "final_font":   current_font,
            "expanded":     False,
            "continuation": False,
            "clipped":      False,
            "cont_rect":    None,
        }

    while fit_remaining < 0 and not resolved:
        # Step 2: check if expanding downward would collide.
        expand_needed = abs(fit_remaining) + current_font
        candidate_bottom = y1 + expand_needed

        if not _would_collide(candidate_bottom):
            # Step 3: no collision — expand rect and re-insert.
            expanded_rect = fitz.Rect(x0, y0, x1, candidate_bottom)
            expand_erase = fitz.Rect(
                x0 - 2, y0 - 2, x1 + 2, expanded_rect.y1 + 4
            )
            _erase_and_redraw(expand_erase)
            page.insert_textbox(
                expanded_rect,
                block_text,
                fontsize=current_font,
                fontname=resolved_font,
                color=(0, 0, 0),
                align=0,
            )
            resolved = True
            expanded = True
        else:
            # Step 4: collision — reduce font size by 1pt and retry from
            # step 1 (original rect), unless already at MIN_FONT.
            if current_font <= MIN_FONT:
                # Cannot reduce further; exit loop for step 5.
                break
            current_font = max(current_font - 1.0, MIN_FONT)
            # Step 1 retry: insert with original rect at new font.
            text_rect_retry = fitz.Rect(x0, y0, x1, y1)
            fit_remaining = page.insert_textbox(
                text_rect_retry,
                block_text,
                fontsize=current_font,
                fontname=resolved_font,
                color=(0, 0, 0),
                align=0,
            )
            if fit_remaining >= 0:
                # Text fits at the reduced font size — done.
                resolved = True

    if not resolved:
        # Step 5: still overflowing at 6pt — continuation box.
        # Find the bottom of the last block on the page.
        if other_bboxes:
            last_bottom = max(ob[3] for ob in other_bboxes)
        else:
            last_bottom = y1

        cont_top    = last_bottom + 4.0
        cont_bottom = cont_top + abs(fit_remaining) + MIN_FONT
        cont_rect   = fitz.Rect(x0, cont_top, x1, cont_bottom)

        # Step 6: clip to page boundary if needed (Req 6.6).
        if cont_bottom > page_height:
            print(
                f"  ⚠️  Continuation box clipped to page boundary "
                f"(page {page_num}, bbox [{x0}, {y0}, {x1}, {y1}])"
            )
            cont_rect = fitz.Rect(x0, cont_top, x1, page_height)
            clipped = True

        # Erase the continuation area and insert remaining text.
        cont_erase = fitz.Rect(
            x0 - 2, cont_rect.y0 - 2, x1 + 2, cont_rect.y1 + 4
        )
        _erase_and_redraw(cont_erase)
        page.insert_textbox(
            cont_rect,
            block_text,
            fontsize=MIN_FONT,
            fontname=resolved_font,
            color=(0, 0, 0),
            align=0,
        )
        continuation = True

    return {
        "resolved":     resolved,
        "final_font":   current_font,
        "expanded":     expanded,
        "continuation": continuation,
        "clipped":      clipped,
        "cont_rect":    cont_rect,
    }


def write_pdf_preserved(blocks, original_pdf_path, output_file):
    """
    Overlay translated text on the original PDF, preserving layout.

    FIX vs previous version:
      - OLD: insert_text() at a single Point → no word-wrap → text overflows
             horizontally off the page for longer translations.
             White rect was sized from the tiny 50-char search bbox.
      - NEW: insert_textbox() with the FULL block bbox → automatic word-wrap
             within the original text area.  If the translated text is longer
             than the original block, the box expands downward and we also
             expand the erasing rect so no artefacts show through.

    Font matching (Requirements 4.1–4.5):
      - Font_Mapper.resolve() is called per block using the font name stored
        in block["style"]["font"] (extracted by read_pdf() from span metadata).
      - The set of embedded font names for each page is obtained via
        fitz.Document.get_page_fonts() and passed to Font_Mapper.resolve().
    """
    original_doc = fitz.open(original_pdf_path)
    output_doc   = fitz.open()

    # Instantiate Font_Mapper and Background_Sampler once; reuse across all pages and blocks.
    font_mapper = Font_Mapper()
    bg_sampler = Background_Sampler()

    for page_num in range(len(original_doc)):
        original_page = original_doc[page_num]

        # New blank page with identical dimensions
        new_page = output_doc.new_page(
            width=original_page.rect.width,
            height=original_page.rect.height
        )

        # Stamp the original page content (images, graphics, background) first
        new_page.show_pdf_page(
            new_page.rect,
            original_doc,
            page_num,
            keep_proportion=True
        )

        # Build the set of font names embedded in this page (Requirement 4.2).
        # get_page_fonts() returns a list of tuples; index 3 is the font name.
        try:
            page_font_tuples = original_doc.get_page_fonts(page_num)
            embedded_fonts: set = {
                tup[3] for tup in page_font_tuples if tup[3]
            }
        except Exception:
            embedded_fonts = set()

        page_blocks = [b for b in blocks if b.get("page") == page_num]

        for block in page_blocks:
            try:
                bbox = block.get("position", [50, 50, 500, 100])
                x0, y0, x1, y1 = bbox

                style     = block.get("style", {})
                font_size = float(style.get("font_size", 11) or 11)
                font_size = max(6.0, min(font_size, 72.0))  # safety clamp

                # ── Resolve font name via Font_Mapper (Requirements 4.1–4.5) ─
                raw_font_name = style.get("font", "helv") or "helv"
                resolved_font = font_mapper.resolve(
                    raw_font_name, embedded_fonts,
                    page=page_num, bbox=bbox
                )

                # ── Step 1: erase original text using background color ──────
                # Sample the background color at the four corners of the block.
                # If all corners share the same color, use it as the fill.
                # Otherwise, use the clip-and-redraw strategy (Req 5.2–5.4).
                # For zero-width/height bboxes, skip erasure (Req 5.5).
                erase_rect = fitz.Rect(x0 - 2, y0 - 2, x1 + 2, y1 + 4)

                if x1 > x0 and y1 > y0:
                    bg_color = bg_sampler.sample(original_page, (x0, y0, x1, y1))
                else:
                    bg_color = None  # zero-width or zero-height — skip erasure (Req 5.5)

                if bg_color is not None:
                    # Uniform background — use sampled color as fill (Req 5.2)
                    new_page.draw_rect(erase_rect, color=None, fill=bg_color)
                elif x1 > x0 and y1 > y0:
                    # Non-uniform background — clip-and-redraw strategy (Req 5.3–5.4)
                    # Re-stamp the original page content clipped to the block region,
                    # then the translated text will be drawn on top.
                    clip_rect = fitz.Rect(x0 - 2, y0 - 2, x1 + 2, y1 + 4)
                    new_page.show_pdf_page(
                        clip_rect,
                        original_doc,
                        page_num,
                        clip=clip_rect,
                        keep_proportion=True,
                    )
                # If zero-width/height, skip erasure entirely (Req 5.5)

                # ── Step 2: insert translated text with word-wrap ───────────
                # insert_textbox() wraps text inside the rect automatically.
                # It returns the amount of space remaining (negative = overflow).
                text_rect = fitz.Rect(x0, y0, x1, y1)

                remaining = new_page.insert_textbox(
                    text_rect,
                    block["text"],
                    fontsize=font_size,
                    fontname=resolved_font,
                    color=(0, 0, 0),
                    align=0,        # 0=left, 1=centre, 2=right
                )

                # ── Steps 1–6: overflow containment (Requirements 6.1–6.6) ──
                # Delegate to _resolve_overflow() which implements the full
                # algorithm and is independently unit-testable.
                if remaining < 0:
                    # Collect bounding boxes of all OTHER blocks on this page
                    # (read-only — we never modify them, Requirement 6.5).
                    other_bboxes = [
                        b.get("position", [])
                        for b in page_blocks
                        if b is not block and b.get("position")
                    ]

                    _resolve_overflow(
                        page=new_page,
                        block_text=block["text"],
                        x0=x0, y0=y0, x1=x1, y1=y1,
                        font_size=font_size,
                        resolved_font=resolved_font,
                        other_bboxes=other_bboxes,
                        page_height=new_page.rect.height,
                        original_doc=original_doc,
                        page_num=page_num,
                        bg_color=bg_color,
                        initial_remaining=remaining,
                    )

            except Exception as e:
                print(f"  ⚠️  Layout error for block: {str(e)[:100]}")
                try:
                    # Best-effort fallback: left margin, approximate y
                    fallback_rect = fitz.Rect(50, y0, x1, y1 + 60)
                    new_page.insert_textbox(
                        fallback_rect,
                        block["text"],
                        fontsize=11,
                        fontname="helv",
                        color=(0, 0, 0),
                    )
                except Exception:
                    pass

    output_doc.save(output_file)
    output_doc.close()
    original_doc.close()
    print("  ✓ PDF saved with layout preservation")


def write_txt(blocks, output_file):
    """Write blocks to plain text."""
    with open(output_file, "w", encoding="utf-8") as f:
        for block in blocks:
            if isinstance(block, dict):
                f.write(block.get("text", "") + "\n\n")


def write_csv(blocks_data, output_file):
    """Write translated CSV data preserving row/column structure."""
    with open(output_file, "w", encoding="utf-8", newline="") as f:
        writer = csv.writer(f)
        writer.writerows(blocks_data)


def choose_output_path(input_file, output_dir=""):
    """Decide output filename based on input format capabilities."""
    base = os.path.splitext(os.path.basename(input_file))[0]
    ext  = os.path.splitext(input_file)[1].lower()
    WRITABLE_FORMATS = {
        ".docx": ".docx",
        ".txt":  ".txt",
        ".md":   ".md",
        ".pdf":  ".pdf",
        ".csv":  ".csv",
        ".rtf":  ".docx",
        ".odt":  ".docx",
    }
    out_ext  = WRITABLE_FORMATS.get(ext, ".docx")
    out_name = f"translated_{base}{out_ext}"
    return os.path.join(output_dir, out_name) if output_dir else out_name


def reconstruct_document(blocks, output_file, original_file=None, original_ext=None):
    """Write translated blocks to the appropriate output format."""
    ext = os.path.splitext(output_file)[1].lower()
    if ext == ".docx":
        write_docx(blocks, output_file, original_file)
    elif ext == ".pdf" and original_file:
        write_pdf_preserved(blocks, original_file, output_file)
    elif ext in {".txt", ".md"}:
        write_txt(blocks, output_file)
    elif ext == ".csv":
        if isinstance(blocks, list) and blocks and "data" in blocks[0]:
            write_csv(blocks[0]["data"], output_file)
        else:
            write_csv(blocks, output_file)
    else:
        write_txt(blocks, output_file)

