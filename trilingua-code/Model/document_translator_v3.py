class BLEU_Reporter:
    """
    Computes a corpus-level BLEU score for a completed translation job.

    Requirements 9.1 – 9.6:
    - Reads a plain-text reference file (one block per non-empty line).
    - Aligns translated blocks and reference lines by index up to
      ``min(len(translated_blocks), len(ref_lines))``.
    - Logs a warning when the counts differ.
    - Returns a ``sacrebleu`` corpus BLEU score in the range 0.0–100.0.
    - Returns ``None`` (and logs a warning) on any read/parse error.
    """

    def compute(
        self,
        translated_blocks: list,
        reference_file: str,
    ) -> "float | None":
        """
        Compute corpus BLEU between *translated_blocks* and *reference_file*.

        Parameters
        ----------
        translated_blocks : list[dict]
            The translated block objects produced by ``batch_translate_blocks()``.
            Each block must have a ``"text"`` key.
        reference_file : str
            Path to a plain-text file with one reference block per non-empty line.

        Returns
        -------
        float | None
            Corpus BLEU score in ``[0.0, 100.0]``, or ``None`` on error.
        """
        try:
            import sacrebleu  # noqa: PLC0415 — imported here to keep top-level optional

            # ── Read reference file ───────────────────────────────────────────
            with open(reference_file, "r", encoding="utf-8", errors="replace") as fh:
                ref_lines = [line.rstrip("\n") for line in fh if line.strip()]

        except Exception as exc:  # noqa: BLE001
            print(
                f"  ⚠️  BLEU_Reporter: cannot read reference file '{reference_file}': "
                f"{exc} — skipping BLEU computation."
            )
            return None

        try:
            # ── Align by index ────────────────────────────────────────────────
            n_hyp = len(translated_blocks)
            n_ref = len(ref_lines)
            n_aligned = min(n_hyp, n_ref)

            if n_hyp != n_ref:
                print(
                    f"  ⚠️  BLEU_Reporter: translated block count ({n_hyp}) differs from "
                    f"reference line count ({n_ref}); computing BLEU over {n_aligned} aligned pairs."
                )

            hypotheses = [translated_blocks[i]["text"] for i in range(n_aligned)]
            references = [ref_lines[i] for i in range(n_aligned)]

            if not hypotheses:
                print(
                    "  ⚠️  BLEU_Reporter: no aligned pairs to score — returning None."
                )
                return None

            # sacrebleu expects references as a list of lists (one list per
            # reference translation; here we have a single reference set).
            result = sacrebleu.corpus_bleu(hypotheses, [references])
            return result.score  # float in [0.0, 100.0]

        except Exception as exc:  # noqa: BLE001
            print(
                f"  ⚠️  BLEU_Reporter: error computing BLEU score: {exc} — returning None."
            )
            return None


def run_pipeline(input_file, source_lang, target_lang,
                 output_file=None, pdf_column_mode="auto", glossary=None,
                 reference_file=None):
    """
    Run the complete translation pipeline.

    pdf_column_mode:
      'auto'   – translate all text found (default)
      'left'   – only translate left-column text  (bilingual source PDFs)
      'right'  – only translate right-column text
      'single' – treat page as one column, no filtering

    glossary: optional list of (source_term, target_term) pairs for
      Glossary_Store. When provided, whole-word substitution is applied
      to each translated block after translation. Raises ValueError if
      the glossary contains duplicate source terms or more than 1000 pairs.

    reference_file: optional path to a plain-text reference translation file
      (one block per non-empty line). When provided, a corpus BLEU score is
      computed at the end of the pipeline and included in the result dict.

    Returns
    -------
    dict
        ``{"output_file": str, "translated_blocks": list, "bleu_score": float | None}``
    """
    if output_file is None:
        output_file = choose_output_path(input_file)

    # Build glossary store (raises ValueError early if glossary is invalid)
    if glossary is not None:
        glossary_store = Glossary_Store(glossary)
    else:
        glossary_store = None

    print("📥 Reading document…")
    data, ext = analyze_document(input_file, pdf_column_mode=pdf_column_mode)

    if ext == ".csv":
        csv_data = data
        print(f"  Found {len(csv_data.get('data', []))} rows in CSV.")
        print("🌍 Translating…")
        translated_rows = []
        for row_idx, row in enumerate(csv_data["data"]):
            translated_row = []
            for col_idx, cell in enumerate(row):
                if cell.strip() and len(cell.split()) >= 2:
                    print(f"  Translating cell [{row_idx+1},{col_idx+1}]...", end="\r")
                    translated_row.append(
                        _translate_single(cell, LANGUAGES[source_lang], LANGUAGES[target_lang])
                    )
                else:
                    translated_row.append(cell)
            translated_rows.append(translated_row)
        print("\n📄 Rebuilding CSV →", output_file)
        write_csv(translated_rows, output_file)
        print("✅ Done!")
        return {
            "output_file": output_file,
            "translated_blocks": [{"data": translated_rows}],
            "bleu_score": None,
        }

    blocks = data
    print(f"  Found {len(blocks)} text block(s) after filtering.")

    if not blocks:
        raise ValueError(
            "No translatable text extracted. "
            "If this is a scanned PDF, OCR is required. "
            "For bilingual PDFs, try pdf_column_mode='left' or 'right'."
        )

    print("🌍 Translating…")
    # Instantiate a Context_Buffer scoped to this document job (Requirement 2.1)
    ctx_buffer = Context_Buffer()
    ctx_buffer.clear()  # ensure clean state at the start of each job

    translated_blocks = batch_translate_blocks(
        blocks, source_lang, target_lang,
        glossary_store=glossary_store,
        context_buffer=ctx_buffer,
    )

    print(f"📄 Rebuilding document → {output_file}")
    reconstruct_document(translated_blocks, output_file, input_file, ext)

    # ── BLEU scoring (Requirement 9.1 – 9.6) ─────────────────────────────────
    bleu_score: "float | None" = None
    if reference_file is not None:
        bleu_reporter = BLEU_Reporter()
        bleu_score = bleu_reporter.compute(translated_blocks, reference_file)
        if bleu_score is not None:
            print(f"  📊 BLEU score: {bleu_score:.2f}")

    print("✅ Done!")
    return {
        "output_file": output_file,
        "translated_blocks": translated_blocks,
        "bleu_score": bleu_score,
    }

